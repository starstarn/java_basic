package chat01;

public class java03_Datatype_basic {
    public static void main(String[] args) {
        //基本数据类型
        //1.整数类型
        //byte:8位
        byte b = 10;
        //short:16位
        short s =10;
        // int: 32位
        int i =10;
        //long:64位
        long lon = 10;

        //2.浮点类型
        //根据计算精度分为：
        //默认情况下，小数点的数据会被识别为double类型
        //float:单精度浮点类型,需要f或F结尾
        float f =1.0f;
        //double:双精度浮点类型
        double d = 2.0;

        //3.字符类型
        //使用符号标识文字内容
        char c = '1';

        //4.布尔类型
        //有两个值，true  false；
        boolean bln = true;

    }
}
